// Contando normal
console.log("Vamos contar");

for (var repetir = 1; repetir <= 5; repetir++) {
  console.log(repetir);
}

console.log("Fim da contagem.");

console.log("Contagem de 5 a 15:");

for (var numero = 5; numero <= 15; numero++) {
  console.log(numero);
}

console.log("Fim da contagem.");

// Contagem regressiva
console.log("Contagem regressiva de 10 a 1:");

for (var reg = 10; reg >= 1; reg--) {
  console.log(reg);
}

console.log("Fim da contagem.");

// Números pares
console.log("Números pares de 0 a 10:");

for (var par = 0; par <= 10; par += 2) {
  console.log(par);
}

console.log("Fim da contagem.");
