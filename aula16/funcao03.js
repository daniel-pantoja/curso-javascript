let v = function (x) {
  return x * 2;
};
console.log(v(8));

let quadrado = function (y) {
  return y * y;
};
console.log(quadrado(4));

let ehPar = function (num) {
  return num % 2 === 0;
};
console.log(ehPar(8));
console.log(ehPar(7));

let maior = function (x, y) {
  return x > y ? x : y;
};
console.log(maior(12, 25));
